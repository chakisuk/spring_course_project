package kr.bit.beans;

public class Course {

    private int course_idx;
    private int subject_idx;
    private int user_idx;

    public int getCourse_idx() {
        return course_idx;
    }

    public void setCourse_idx(int course_idx) {
        this.course_idx = course_idx;
    }

    public int getSubject_idx() {
        return subject_idx;
    }

    public void setSubject_idx(int subject_idx) {
        this.subject_idx = subject_idx;
    }

    public int getUser_idx() {
        return user_idx;
    }

    public void setUser_idx(int user_idx) {
        this.user_idx = user_idx;
    }
}
